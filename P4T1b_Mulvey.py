import turtle
    # Enables the use of turtle
t = turtle. Turtle()
t.write("SM" ,font=("Times New Roman",56,"normal"))
    # Creates text of my initials

t.pensize(10)
t.shape("turtle")
t.color("green")
    # Sets the format of the sketch
    
t.backward(100)
t.right(90)
t.forward(100)
t.left(90)
t.forward(100)
t.right(90)
t.forward(100)
t.right(90)
t.forward(100)
    # Creates the "S"

t.up()
t.backward(200)
t.down()
    # Stops drawing lines and moves over to start 2nd initial

t.right(90)
t.forward(200)
t.right(135)
t.forward(100)
t.left(90)
t.forward(100)
t.right(135)
t.forward(200)
    # Creates the "M"
